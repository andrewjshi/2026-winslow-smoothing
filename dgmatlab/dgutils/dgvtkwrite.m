function dgvtkwrite(fname, viz)
%DGVTKWRITE Export high-order DG data to VTK / Paraview
%    
%   Example: 3-D Mesh only
%      nref = 3; porder = 4;
%      msh = mshsphere(nref);
%      msh = nodealloc(msh, porder);
%      dgvtkwrite('sphere_tetmesh.vtk', dgviz_vol(msh));
%
%   Example: 2-D Navier-Stokes, volumetric
%      duct1
%      viz = dgviz_vol(msh, u, 2);
%      dgvtkwrite('duct1.vtk' , viz);
%
%   Example: 3-D Convection-Diffusion, volumetric + boundary fluxes
%      cd5
%      viz1 = dgviz_vol(msh, u);
%      viz2 = dgviz_bnd(msh, u, @dgconvdiff, phys, 1, 'all');
%      dgvtkwrite('cd5vol.vtk', viz1);
%      dgvtkwrite('cd5bndflux.vtk', viz2);
    
    np = size(viz.p,1);
    [nt,ns] = size(viz.t);
    dim = viz.dim;
    
    [map,celltype] = node_order_map(dim, viz.porder, viz.eltype);
    viz.t(:,map) = viz.t;
    
    fid=fopen(fname,'w');
    fprintf(fid,'# vtk DataFile Version 4.2\n');
    fprintf(fid,'DG\n');
    fprintf(fid,'ASCII\n');
    fprintf(fid,'DATASET UNSTRUCTURED_GRID\n');
    fprintf(fid,'POINTS %d float\n', np);
    fclose(fid);
    
    if size(viz.p,2) == 2
        viz.p = cat(2, viz.p, 0*viz.p(:,1));
    end
    dlmwrite(fname, viz.p, '-append', 'delimiter',' ');
    
    fid=fopen(fname,'a');
    fprintf(fid,'\nCELLS %d %d\n', nt, nt*(1+ns));
    fclose(fid);
    dlmwrite(fname, cat(2, 0*viz.t(:,1)+ns, viz.t-1), ...
             '-append','precision','%d','delimiter',' ');
    
    fid=fopen(fname,'a');
    fprintf(fid,'\nCELL_TYPES %d\n', nt);
    fclose(fid);
    
    dlmwrite(fname, 0*viz.t(:,1) + celltype, '-append', 'precision','%d', 'delimiter',' ');
    
    fid=fopen(fname,'a');
    fprintf(fid,'\nPOINT_DATA %d\n', np);
    fclose(fid);

    if isfield(viz, 'u') & ~isempty(viz.u)
        if isnumeric(viz.u)
            % General case, just dump all the solution components
            mk_vtk_data(fname, 'SCALARS', 'scalar_data', viz.u)
        elseif iscell(viz.u)
            % Detailed format
            for cu = viz.u
                mk_vtk_data(fname, upper(cu{1}{1}), cu{1}{2}, cu{1}{3});
            end
        end
    end
    
end

function mk_vtk_data(fname, typename, uname, u)
    fid=fopen(fname,'a');
    if all(typename=='VECTORS')
        fprintf(fid,'%s %s float\n', typename, uname);
        if size(u,2) == 2
            u = cat(2, u, 0*u(:,1));
        end
    else
        fprintf(fid,'%s %s float %d\n', typename, uname, size(u,2));
        fprintf(fid,'LOOKUP_TABLE default\n');
    end
    fclose(fid);
    dlmwrite(fname,u,'-append','delimiter',' ');
end

